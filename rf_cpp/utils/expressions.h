#ifndef RF_CPP_EXPRESSIONS_H
#define RF_CPP_EXPRESSIONS_H

#endif //RF_CPP_EXPRESSIONS_H

template <typename T>
bool greater_than(T x, T y) {
    return x > y;
}

template <typename T>
bool less_than(T x, T y) {
    return x < y;
}

template <typename T>
bool greater_equal(T x, T y) {
    return x >= y;
}

template <typename T>
bool less_equal(T x, T y) {
    return x <= y;
}

template <typename T>
bool equal_to(T x, T y) {
    return x == y;
}