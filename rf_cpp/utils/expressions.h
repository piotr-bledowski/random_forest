bool greater_than(long x, long y) {
    return x > y;
}

bool less_than(long x, long y) {
    return x < y;
}

bool greater_equal(long x, long y) {
    return x >= y;
}

bool less_equal(long x, long y) {
    return x <= y;
}

bool equal_to(long x, long y) {
    return x == y;
}

